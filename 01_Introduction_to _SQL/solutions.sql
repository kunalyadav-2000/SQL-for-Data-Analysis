-- 2. Declarative means you describe the result you want (e.g., "show all customers from London") without specifying the steps the computer must take to retrieve the data. The database engine handles the *how*.

-- 3. SQL is the language; PostgreSQL is the database management system (DBMS) that understands and executes SQL, stores the data, and manages access.